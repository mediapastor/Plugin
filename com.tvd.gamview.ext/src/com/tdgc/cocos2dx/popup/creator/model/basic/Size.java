package com.tdgc.cocos2dx.popup.creator.model.basic;

public class Size {
	
	public Size(float pWidth, float pHeight) {
		this.mWidth = pWidth;
		this.mHeight = pHeight;
	}

	public float getWidth() {
		return this.mWidth;
	}
	
	public float getHeight() {
		return this.mHeight;
	}
	
	protected float mWidth;
	protected float mHeight;
	
}
